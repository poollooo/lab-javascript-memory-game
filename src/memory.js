class MemoryGame {
  constructor(cards) {
    this.cards = cards;
    this.pickedCards = [];
    this.pairsClicked = 0;
    this.pairsGuessed = 0;
  }

  shuffleCards(cards) {
    let array = [...this.cards]
    let i = array.length;
    if (!cards) {
      return undefined;
    }
    while (--i > 0) {
      let randIndex = Math.floor(Math.random() * (i + 1));
      [array[randIndex], array[i]] = [array[i], array[randIndex]];
    }
    return array;
  }

  checkIfPair(card1, card2) {
    this.pairsClicked = 1;
    if (card1 === card2) {
      this.pairsGuessed += 1;
      return true;
    } else {
      return false;
    }
  }

  checkIfFinished() {
    if (this.pairsGuessed === 0) {
      return false;
    } else if (this.pairsGuessed !== (this.cards.length / 2)) {
      return false;
    } else {
      return true
    }
  }
}

